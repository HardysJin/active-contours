function [x, y] = initializeSnake(I)


    % h = drawfreehand;

    % x = h.Position(:, 1);
    % y = h.Position(:, 2);
    


    % shape
%     x = [90.8750 49.3750 59.8750 78.3750 94.3750 179.8750 236.3750 274.3750 275.3750 263.8750 227.3750 176.8750 136.3750]';
%     y = [48.6250 98.6250 188.6250 254.1250 304.1250 273.6250 286.6250 246.1250 177.1250 113.6250 52.6250 48.1250 59.1250]';
    % x = [85.3750 64.3750 80.8750 112.8750 118.8750 136.8750 154.8750 168.8750 173.8750 183.3750 201.3750 222.8750 241.8750 249.3750 251.3750 244.3750 226.8750 198.8750 155.8750 114.8750]';
    % y = [248.3750 150.8750 88.3750 69.8750 105.8750 138.3750 160.8750 137.8750 111.3750 71.3750 75.8750 98.3750 134.8750 184.8750 218.3750 248.3750 270.8750 248.3750 268.8750 276.8750]';
    % % cilcle + square
    % x  = [46.8750 18.3750 16.3750 54.8750 113.8750 206.3750 246.8750 237.3750 139.3750]';
    % y = [28.1250 89.1250 169.6250 228.1250 249.1250 244.6250 161.1250 40.1250 13.6250]';

    % Show figure
    imshow(I,'InitialMagnification', 300);
    % Get initial points
    [x, y] = getpts;
    
    % close - end point = start point
    x = [x; x(1)]; 
    y = [y; y(1)];

    % Interpolate 
    numPts = size(x, 1);
    steps = 1:0.05:numPts;
    x = spline(1:numPts, x, steps);
    y = spline(1:numPts, y, steps);

    % Clamp points to be inside of image
    [h, w] = size(I); % image height & width
    % x(x > w) = w; % Logical Indexing
    % x(x < 1) = 1;
    % y(y > h) = h;
    % y(y < 1) = 1;
    
    x = max(x, 1);
    x = min(x, w);
    y = max(y, 1);
    y = min(y, h);

end

